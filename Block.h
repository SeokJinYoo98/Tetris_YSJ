class Blocks {
	int Bx{};
	int By{};
	int RotationCnt{ 0 };
	int shape[4][4][4]{ };
	int BlockCnt{ };

public:
	Blocks(int num);
	~Blocks();

	void BMoveR() { ++Bx; };
	void BMoveL() { --Bx; };
	void BMoveD() { ++By; };
	void BRotate() { RotationCnt = ++RotationCnt % 4;};

	void SetBx(int temp) { Bx = temp; };
	void SetBy(int temp) { By = temp; };
	void SetRCnt(int temp) { RotationCnt = temp; };

	inline int GetBx() const { return Bx; };
	inline int GetBy() const { return By; };
	inline int GetRotationCnt() const { return RotationCnt; };

	inline int GetShape(int x, int y) const { return shape[RotationCnt][y][x]; }



};

